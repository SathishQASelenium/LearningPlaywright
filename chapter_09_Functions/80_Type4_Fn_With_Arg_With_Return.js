// Type 4 Function: With Argument With Return Value

function sumofTwoNumbers(a, b) {
    return a + b;
}

let c = sumofTwoNumbers(4, 5); // 4 and 5 are arguments, a and b are parameters
let c2 = sumofTwoNumbers(10, 15); // 10 and 15 are arguments, a and b are parameters
console.log(c); // Output: 9
console.log(c2); // Output: 25